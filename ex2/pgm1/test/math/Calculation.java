package math;
public class Calculation{
/** The calulation class implements,the basic operation such as addtion ,subtraction,
    multiplication and division . These are created inside the user defined package called math.
    @author aumrudh
    @since 2019-12-19
*/
//add method simply add two integers and return
public int add(int x, int y){
    return x+y;
}
//sub method calculate the differance and return
public int sub(int x, int y){
    return x-y;
}
//mul method calculate the product and return
public int mul(int x, int y){
    return x*y;
}
//div method returns the quotient of two integers
public int div(int x, int y){
    return x/y;
}
}

